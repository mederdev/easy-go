export function useDocsModel() {
  // В админке контент прокручивается в обычном контейнере `.content`
  // (AdminLayout), поэтому нативный `scrollIntoView` работает штатно.
  function scrollTo(id: string): void {
    document.getElementById(id)?.scrollIntoView({ behavior: 'smooth', block: 'start' });
  }

  return { sections, scrollTo };
}
