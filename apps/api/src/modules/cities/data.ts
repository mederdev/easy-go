/** Case-insensitive substring search, max 10 results. */
export function searchCities(q: string): string[] {
  const query = q.trim().toLowerCase();
  if (!query || query.length < 2) return [];
  return CITY_LIST.filter((c) => c.toLowerCase().includes(query)).slice(0, 10);
}
